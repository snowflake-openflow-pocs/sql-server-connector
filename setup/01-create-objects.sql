-- Create database for CDC destination
CREATE DATABASE IF NOT EXISTS POC_CDC;

-- Create warehouse for CDC processing
CREATE WAREHOUSE IF NOT EXISTS CDC_DEMO_WH
  WITH WAREHOUSE_SIZE = XSMALL
  AUTO_SUSPEND = 60
  AUTO_RESUME = TRUE;

-- Create image repository for container images
CREATE IMAGE REPOSITORY IF NOT EXISTS POC_CDC.PUBLIC.IMAGES;

-- Create compute pool for SPCS service
CREATE COMPUTE POOL IF NOT EXISTS CDC_DEMO_POOL
  MIN_NODES = 1
  MAX_NODES = 1
  INSTANCE_FAMILY = CPU_X64_S
  AUTO_RESUME = TRUE
  AUTO_SUSPEND_SECS = 3600;

-- Create secret for SQL Server SA password
CREATE SECRET IF NOT EXISTS POC_CDC.PUBLIC.SQL_SA_PASSWORD
  TYPE = PASSWORD
  USERNAME = 'sa'
  PASSWORD = 'YourStrongPassw0rd!';